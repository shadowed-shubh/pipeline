// --- AUDIO SUMMARY ---
async function speakSummary() {
    const textEl = document.getElementById('result-summary-text');
    if (!textEl) return;
    const text = textEl.innerText;
    
    const btn = document.getElementById('btn-listen-summary');
    if (btn) {
        btn.innerHTML = `<span style="font-size:1.1rem; animation: pulse 1s infinite;">🔊</span> <span>Playing...</span>`;
    }

    try {
        const res = await api.getVoiceReport(text);
        const audio = new Audio(URL.createObjectURL(res));
        audio.play();
        audio.onended = () => {
            if (btn) {
                btn.innerHTML = `<i data-lucide="volume-2" style="width: 18px; height: 18px;"></i> <span>Listen to Summary</span>`;
                lucide.createIcons();
            }
        };
        return;
    } catch(err) {
        console.warn("Backend voice failed, using TTS fallback");
    }

    if (!('speechSynthesis' in window)) {
        alert("Audio not supported, please read summary.");
        if(btn) { btn.innerHTML = `<i data-lucide="volume-2" style="width: 18px; height: 18px;"></i> <span>Listen to Summary</span>`; lucide.createIcons(); }
        return;
    }

    const speech = new SpeechSynthesisUtterance();
    speech.text = text;
    speech.lang = "en-IN";
    speech.rate = 1;
    speech.pitch = 1;

    speech.onend = () => {
        if (btn) {
            btn.innerHTML = `<i data-lucide="volume-2" style="width: 18px; height: 18px;"></i> <span>Listen to Summary</span>`;
            lucide.createIcons();
        }
    };

    speech.onerror = () => {
        if (btn) {
            btn.innerHTML = `<i data-lucide="volume-2" style="width: 18px; height: 18px;"></i> <span>Listen to Summary</span>`;
            lucide.createIcons();
        }
        alert("Audio not supported, please read summary.");
    }

    window.speechSynthesis.cancel();
    window.speechSynthesis.speak(speech);
}

