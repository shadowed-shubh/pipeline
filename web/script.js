// --- API INTEGRATION ---
const API_BASE = "https://pipeline-1-ch5e.onrender.com";

const api = {
    getToken() { return localStorage.getItem('token'); },
    clearToken() { localStorage.removeItem('token'); },
    async request(endpoint, options = {}) {
        const headers = { ...options.headers };
        
        // Auto-add JSON header if body is an object and not FormData
        if (options.body && typeof options.body === 'object' && !(options.body instanceof FormData)) {
            headers['Content-Type'] = 'application/json';
            options.body = JSON.stringify(options.body);
        }
        
        // Auto-add Auth header if auth is true
        if (options.auth) {
            const token = this.getToken();
            if(!token) {
                this.handleUnauthorized();
                throw new Error("No auth token found. Please log in.");
            }
            headers['Authorization'] = `Bearer ${token}`;
        }
        
        const fetchOptions = { ...options, headers };
        
        try {
            const res = await fetch(`${API_BASE}${endpoint}`, fetchOptions);
            
            if (res.status === 401) {
                this.handleUnauthorized();
                throw new Error("Unauthorized. Please log in again.");
            }
            if (res.status === 422) {
                throw new Error("Validation Error: Please check your input.");
            }
            if (res.status >= 500) {
                throw new Error("Server Error: Please try again later.");
            }
            if (!res.ok) {
                throw new Error(`API Error: ${res.statusText}`);
            }
            
            return res;
        } catch(err) {
            if (err.message === "Failed to fetch") {
                throw new Error("Network Error: Cannot connect to server.");
            }
            throw err;
        }
    },
    handleUnauthorized() {
        this.clearToken();
        logout();
    },
    
    // ENDPOINTS
    async ping() {
        return this.request('/', { method: 'GET' });
    },
    async register(data) {
        const res = await this.request('/register', { method: 'POST', body: data });
        return res.json();
    },
    async login(email, password) {
        const res = await this.request('/login', { method: 'POST', body: { email, password } });
        const data = await res.json();
        if(data.token) {
            localStorage.setItem('token', data.token);
        }
        return data;
    },
    async getHistory() {
        const res = await this.request('/history', { method: 'GET', auth: true });
        return res.json();
    },
    async addHistory(data) {
        const res = await this.request('/history', { method: 'POST', auth: true, body: data });
        return res.json();
    },
    async getDoctors() {
        const res = await this.request('/doctors', { method: 'GET' });
        return res.json();
    },
    async diagnose(formData) {
        const res = await this.request('/diagnose', { method: 'POST', body: formData });
        return res.json();
    },
    async getVoiceReport(text) {
        // user's prompt: GET /voice-report
        const res = await this.request('/voice-report', { method: 'GET' });
        return res.blob();
    }
};

