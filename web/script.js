function updateUIStrings() {
    const elements = document.querySelectorAll('[data-t]');
    elements.forEach(el => {
        const key = el.getAttribute('data-t');
        if (translations[state.lang][key]) {
            el.innerText = translations[state.lang][key];
        }
    });
}

function changeLanguage(lang) {
    state.lang = lang;
    updateUIStrings();
}

// --- AUTH UI LOGIC ---
function toggleAuthMode(mode) {
    state.authMode = mode;
    
    const titleEl = document.getElementById('auth-title');
    
    // Switch visibility of forms
    document.getElementById('login-form').classList.toggle('hidden', mode === 'signup');
    
    if (mode === 'signup') {
        if (titleEl) {
            titleEl.setAttribute('data-t', 'titleSignUp');
            titleEl.innerText = translations[state.lang].titleSignUp || "Create Account";
        }
        document.getElementById('patient-signup-form').classList.toggle('hidden', state.role !== 'patient');
        document.getElementById('doctor-signup-form').classList.toggle('hidden', state.role !== 'doctor');
        
        // Update footer text for Signup -> Login
        document.getElementById('auth-footer-text').innerHTML = `Already have an account? <a href="#" onclick="toggleAuthMode('login')" style="color: var(--primary); font-weight: 600; text-decoration: none;">Sign in</a>`;
    } else {
        if (titleEl) {
            titleEl.setAttribute('data-t', 'titleSignIn');
            titleEl.innerText = translations[state.lang].titleSignIn || "Sign In";
        }
        document.getElementById('patient-signup-form').classList.add('hidden');
        document.getElementById('doctor-signup-form').classList.add('hidden');
        
        // Update footer text for Login -> Signup
        document.getElementById('auth-footer-text').innerHTML = `Don't have an account? <a href="#" onclick="toggleAuthMode('signup')" style="color: var(--primary); font-weight: 600; text-decoration: none;">Sign up</a>`;
    }
    lucide.createIcons();
}

