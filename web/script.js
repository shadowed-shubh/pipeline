// --- TOAST SYSTEM ---
function showToast(msg, isError = false, duration = 5000) {
    let container = document.getElementById('toast-container');
    if (!container) {
        container = document.createElement('div');
        container.id = 'toast-container';
        container.style = "position: fixed; bottom: 2rem; right: 2rem; z-index: 9999; display: flex; flex-direction: column; gap: 1rem;";
        document.body.appendChild(container);
    }
    
    const toast = document.createElement('div');
    toast.className = 'app-toast';
    toast.style = `background: ${isError ? 'var(--bg-white)' : 'var(--bg-white)'}; border-left: 4px solid ${isError ? 'var(--error)' : 'var(--success)'}; color: var(--text-main); font-weight: 600; padding: 1rem 1.5rem; border-radius: var(--radius-md); box-shadow: 0 8px 24px rgba(0,0,0,0.15); transform: translateX(100%); opacity: 0; transition: all 0.3s cubic-bezier(0.175, 0.885, 0.32, 1.275);`;
    toast.innerText = msg;
    
    container.appendChild(toast);
    
    // Animate in
    requestAnimationFrame(() => {
        toast.style.transform = 'translateX(0)';
        toast.style.opacity = '1';
    });
    
    // Auto remove
    if (duration > 0) {
        setTimeout(() => {
            if (document.body.contains(toast)) {
                toast.style.transform = 'translateX(100%)';
                toast.style.opacity = '0';
                setTimeout(() => toast.remove(), 300);
            }
        }, duration);
    }
    
    return toast;
}

function removeToast(toast) {
    if (toast && document.body.contains(toast)) {
        toast.style.transform = 'translateX(100%)';
        toast.style.opacity = '0';
        setTimeout(() => toast.remove(), 300);
    }
}

