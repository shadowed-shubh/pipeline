function sendChatMessage() {
    const input = document.getElementById('chat-query');
    const text = input.value.trim();
    if (!text) return;

    addMessage('user', text);
    input.value = '';

    // Mock Response
    setTimeout(() => {
        const response = getMockAIResponse(text);
        addMessage('bot', response);
        // TTS
        const utter = new SpeechSynthesisUtterance(response);
        window.speechSynthesis.speak(utter);
    }, 1000);
}

function addMessage(type, text) {
    const chat = document.getElementById('chat-messages');
    const div = document.createElement('div');
    div.className = `msg ${type}`;
    div.innerText = text;
    chat.appendChild(div);
    chat.scrollTop = chat.scrollHeight;
}

function getMockAIResponse(q) {
    q = q.toLowerCase();
    if (q.includes('hello')) return "Hello! I am Auralysis AI. I can help you understand your MRI, X-Ray or blood reports. What would you like to know?";
    if (q.includes('heart')) return "Your cardiomediastinal silhouette appears normal. This indicates no immediate structural concerns with the heart.";
    if (q.includes('doctor')) return "Dr. Mitchell is currently online. You can share your report directly with them from the Results page.";
    return "I recommend reviewing the 'Plain English Explanation' section of your report for a simple breakdown of the findings.";
}

function startVoiceInput() {
    if (!('webkitSpeechRecognition' in window)) {
        alert("Voice recognition not supported in this browser.");
        return;
    }
    const recognition = new webkitSpeechRecognition();
    recognition.lang = state.lang === 'hi' ? 'hi-IN' : state.lang === 'mr' ? 'mr-IN' : 'en-US';
    
    recognition.onstart = () => {
        state.isListening = true;
        document.getElementById('voice-btn').classList.add('active');
    };
    recognition.onend = () => {
        state.isListening = false;
        document.getElementById('voice-btn').classList.remove('active');
    };
    recognition.onresult = (event) => {
        const transcript = event.results[0][0].transcript;
        document.getElementById('chat-query').value = transcript;
        sendChatMessage();
    };
    recognition.start();
}

